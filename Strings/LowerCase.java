package Strings;

public class  LowerCase {
    public static void main(String[] args){
        String strInUpperCase = "FUNNY";
        System.out.println("String before converting to Lowercase:"+ " " + strInUpperCase);
        String strInLowerCase = strInUpperCase.toLowerCase();
        System.out.println("String after converting to Lowercase:" + " " + strInLowerCase );
    }
}
